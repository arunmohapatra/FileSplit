package com.progress.ubroker.util;

import com.progress.common.ehnlog.IAppLogger;


/*********************************************************************/
/*                                                                   */
/* Class RequestQueue                                                */
/*                                                                   */
/*********************************************************************/

public class RequestQueue
	extends Queue
{

/*********************************************************************/
/* Instance Data                                                     */
/*********************************************************************/


/*********************************************************************/
/* Constructors                                                      */
/*********************************************************************/

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public RequestQueue(String queueName, int queueLimit, IAppLogger log)
    {
    super(queueName, queueLimit, log);
    }

/*********************************************************************/
/* Public methods                                                    */
/*********************************************************************/

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public synchronized void enqueueRequest(Request rq)
    throws Queue.QueueException
    {
    rq.settsEnqueue();
    enqueue(rq);
    }

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public synchronized void enqueuePriorityRequest(Request rq)
    throws Queue.QueueException
    {
    rq.settsEnqueue();
    enqueuePriority(rq);
    }

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public synchronized Request dequeueRequest()
    {
    Request rq;

    rq = (Request) dequeue();
    rq.settsDequeue();

    return rq;
    }

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public synchronized Request pollRequest(long timeout)
    {
    Request rq;

    rq = (Request) poll(timeout);

    if (rq != null)
        rq.settsDequeue();

    return rq;
    }

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

/*********************************************************************/
/* Private methods                                                   */
/*********************************************************************/

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

}  /* end of RequestQueue */


