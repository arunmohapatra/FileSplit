package com.progress.ubroker.util;

import com.progress.common.ehnlog.AppLogger;

/*********************************************************************/
/*                                                                   */
/* Class ubThreadQueue                                               */
/*                                                                   */
/*********************************************************************/

public class ubThreadQueue
	extends Queue
{

/*********************************************************************/
/* Instance Data                                                     */
/*********************************************************************/


/*********************************************************************/
/* Constructors                                                      */
/*********************************************************************/

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public ubThreadQueue(String queueName, int queueLimit, AppLogger log)
    {
    super(queueName, queueLimit, log);
    }

/*********************************************************************/
/* Public methods                                                    */
/*********************************************************************/

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public synchronized void enqueueThread(ubThread ubt)
    throws Queue.QueueException
    {
    enqueue(ubt);
    }

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public synchronized ubThread dequeueThread()
    {
    ubThread ubt;

    ubt = (ubThread) dequeue();

    return ubt;
    }

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

/*********************************************************************/
/* Private methods                                                   */
/*********************************************************************/

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

}  /* end of ubThreadQueue */


