package com.progress.ubroker.util;

/*********************************************************************/
/*                                                                   */
/* Class ObjectHolder                                                */
/*                                                                   */
/*********************************************************************/

public class ObjectHolder
{

/*********************************************************************/
/* Constants                                                         */
/*********************************************************************/

/*********************************************************************/
/* Static Data                                                       */
/*********************************************************************/

/*********************************************************************/
/* Instance Data                                                     */
/*********************************************************************/

private Object obj;

/*********************************************************************/
/* public methods                                                    */
/*********************************************************************/

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public ObjectHolder ()
    {
	obj = null;
    }

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public ObjectHolder (Object obj)
    {
	this.obj = obj;
    }

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public void setObject(Object obj) 
    {
	this.obj = obj;
    }

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public Object getObject() 
    {
	return obj;
    }

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

public boolean isNull()
    {
	return (obj == null);
    }

/*********************************************************************/
/*                                                                   */
/*********************************************************************/

}    /* ObjectHolder */
